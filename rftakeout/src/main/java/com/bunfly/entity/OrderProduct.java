package com.bunfly.entity;
/**
 * 
 * 订单详情类
 *
 */
public class OrderProduct {
	private Integer id;
	private Integer proid;
	private Integer pronum;
	private float propri;
	private float propriall;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getProid() {
		return proid;
	}
	public void setProid(Integer proid) {
		this.proid = proid;
	}
	public Integer getPronum() {
		return pronum;
	}
	public void setPronum(Integer pronum) {
		this.pronum = pronum;
	}
	public float getPropri() {
		return propri;
	}
	public void setPropri(float propri) {
		this.propri = propri;
	}
	public float getPropriall() {
		return propriall;
	}
	public void setPropriall(float propriall) {
		this.propriall = propriall;
	}
	
}
